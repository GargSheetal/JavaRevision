package loops;

public class WhileLoop {

	public static void main(String[] args) {
		
		// Print 1-10
		int i=1;
		
		while(i<=10)
		{
			System.out.println(i);	// 1 2 3 4......10
			i++;	//i=i+1
		}
		
		System.out.println("Program Exited");
		
		
		// Print only even numbers
		i=2;
		
		while(i<=10)
		{
			System.out.println(i);	// 1 2 3 4......10
			i+=2;	//i=i+2
		}
		
		System.out.println("Program Exited");
		
		
		

	}

}
