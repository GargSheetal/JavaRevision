package conditionalStatements;

public class EvenOrOdd {

	public static void main(String[] args) {
		
		int number;
		
		for(number=1; number<=10; number++)
		{
			if(number%2==0)
			{
				System.out.println(number + " Number is even");
			}
			else
			{
				System.out.println(number + " Number is odd");
			}
			
		}
				
		

	}

}
